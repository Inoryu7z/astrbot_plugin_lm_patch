# ============================================================
# JSON 提取工具
# ============================================================

def extract_json(text: str) -> dict | None:
    """从 LLM 输出中提取 JSON 对象。

    处理以下情况：
    - 纯 JSON
    - ```json ... ``` 包裹
    - ``` ... ``` 包裹
    - JSON 前后有额外文本
    """
    text = text.strip()

    # 尝试直接解析
    try:
        return json.loads(text)
    except Exception:
        pass

    # 尝试提取 ```json ... ``` 或 ``` ... ``` 中的内容
    patterns = [
        r"```json\s*(.*?)\s*```",
        r"```\s*(.*?)\s*```",
    ]
    for pattern in patterns:
        match = re.search(pattern, text, re.DOTALL)
        if match:
            try:
                return json.loads(match.group(1).strip())
            except Exception:
                pass

    # 尝试提取第一个 { ... } 块
    match = re.search(r"\{.*\}", text, re.DOTALL)
    if match:
        try:
            return json.loads(match.group(0))
        except Exception:
            pass

    return None
